package it.hellokitty.gt.xmlstructure.country;

public class CountryXML {
	private String description;
	private String regionId;
	private String countryId;
	private String countryIdISO3166_3;
	
	public String getDescription() {
		return description;
	}
	
	public void setDescription(String description) {
		this.description = description;
	}
	
	public String getRegionId() {
		return regionId;
	}
	
	public void setRegionId(String regionId) {
		this.regionId = regionId;
	}
	
	public String getCountryId() {
		return countryId;
	}
	
	public void setCountryId(String countryId) {
		this.countryId = countryId;
	}
	
	public String getCountryIdISO3166_3() {
		return countryIdISO3166_3;
	}
	
	public void setCountryIdISO3166_3(String countryIdISO3166_3) {
		this.countryIdISO3166_3 = countryIdISO3166_3;
	}
}
